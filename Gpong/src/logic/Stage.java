package logic;

/**
 *
 * @author 41407
 */
public class Stage {

    private double height;
    private double width;

    public Stage(double width, double height) {
        this.height = height;
        this.width = width;
    }

    public double getHeight() {
        return this.height;
    }

    public double getWidth() {
        return width;
    }
}
